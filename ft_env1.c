#include "ft_minishell.h"

int				ft_env_check(char *w_splited)
{
	if (w_splited[0] == '$')
		return (1);
	return (0);
}

char			**envp_create(char **env, char **envp)
{
	int i;

	i = 0;
	while (envp[i])
	{
		env[i] = ft_strdup(envp[i]);
		i++;
	}
	env[i] = NULL;
	return (env);
}